//BEGIN ALGO
char s[MAXN];
int pi[MAXN];

void getpi(char * s, int n, int * pi) {
    pi[0] = 0;
    for (int i = 1; i < n; ++i) {
        int j = pi[i - 1];
        while (j > 0 && s[i] != s[j])
            j = pi[j - 1];
        if (s[i] == s[j])
            ++j;
        pi[i] = j;
    }
}

int main() {
    scanf("%s", s);
    int n = strlen(s);
    getpi(s, n, pi);
    return 0;
}
